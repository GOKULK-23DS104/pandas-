"""•Syntax-Pandas Create Dataframe Syntax
 pandas.DataFrame(data, index, columns)
 Examples:
 •Using Empty DataFrame
 •Using Lists or Arrays
 •Using List of Dictionaries
 •Using Another DataFrame
 •Using Dictionary of Series
 •Using zip() function
 •Using csv file"""
"""Parameters:
 data: It is a dataset from which a DataFrame is to be created. It can
be a list, dictionary, scalar value, series, and arrays, etc.
 index: It is optional, by default the index of the DataFrame starts
from 0 and ends at the last data value(n-1). It defines the row label
explicitly.
 columns: This parameter is used to provide column names in the
DataFrame. If the column name is not defined by default, it will take
a value from 0 to n-1."""